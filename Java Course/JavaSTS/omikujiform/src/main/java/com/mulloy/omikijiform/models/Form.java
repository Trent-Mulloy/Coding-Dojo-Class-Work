package com.mulloy.omikijiform.models;

public class Form {
	private int num;
	private String city;
	private String person;
	private String profession;
	private String thing;
	private String nice_thing;
	
	
	
	
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
	public String getProfession() {
		return profession;
	}
	public void setProfession(String profession) {
		this.profession = profession;
	}
	public String getThing() {
		return thing;
	}
	public void setThing(String thing) {
		this.thing = thing;
	}
	public String getNice_thing() {
		return nice_thing;
	}
	public void setNice_thing(String nice_thing) {
		this.nice_thing = nice_thing;
	}
	
	
	
	

}
